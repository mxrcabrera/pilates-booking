'use client'

import { useState } from 'react'
import { useRouter } from 'next/navigation'
import { loginWithGoogle } from './actions'
import { getErrorMessage } from '@/lib/utils'

type Rol = 'profesor' | 'alumno' | null

export function LoginForm() {
  const [isLoading, setIsLoading] = useState(false)
  const [isSignup, setIsSignup] = useState(false)
  const [error, setError] = useState<string | null>(null)
  const [showPassword, setShowPassword] = useState(false)
  const [rol, setRol] = useState<Rol>(null)
  const router = useRouter()

  async function handleSubmit(e: React.FormEvent<HTMLFormElement>) {
    e.preventDefault()
    setIsLoading(true)
    setError(null)

    const formData = new FormData(e.currentTarget)
    const email = formData.get('email') as string
    const password = formData.get('password') as string
    const nombre = formData.get('nombre') as string

    if (isSignup) {
      if (!rol) {
        setError('Seleccioná si sos profesor/a o alumno/a')
        setIsLoading(false)
        return
      }

      if (!nombre || nombre.trim().length < 2) {
        setError('El nombre debe tener al menos 2 caracteres')
        setIsLoading(false)
        return
      }
    }

    if (!email || !email.includes('@')) {
      setError('Ingresá un email válido')
      setIsLoading(false)
      return
    }

    if (!password || password.length < 8) {
      setError('La contraseña debe tener al menos 8 caracteres')
      setIsLoading(false)
      return
    }

    try {
      const response = await fetch('/api/auth/login', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          email,
          password,
          nombre: isSignup ? nombre : undefined,
          rol: isSignup ? rol : undefined,
          action: isSignup ? 'signup' : 'login',
        }),
      })

      const data = await response.json()

      if (!response.ok) {
        throw new Error(data.error || 'Error al procesar la solicitud')
      }

      if (data.redirectTo) {
        router.push(data.redirectTo)
        router.refresh()
      }
    } catch (err) {
      setError(err instanceof Error ? getErrorMessage(err) : 'Error al procesar la solicitud')
      setIsLoading(false)
    }
  }

  return (
    <div className="auth-container">
      <div className="auth-card">
        <h1 className="auth-title">Pilates Booking</h1>
        <p className="auth-subtitle">
          {isSignup ? 'Creá tu cuenta' : 'Iniciá sesión en tu cuenta'}
        </p>

        {error && (
          <div className="auth-error">{error}</div>
        )}

        <form onSubmit={handleSubmit} className="auth-form">
          {isSignup && (
            <>
              <div className="auth-field">
                <label className="auth-label">Soy...</label>
                <div className="auth-rol-grid">
                  <button
                    type="button"
                    className={`auth-rol-option ${rol === 'profesor' ? 'selected' : ''}`}
                    onClick={() => setRol('profesor')}
                    disabled={isLoading}
                  >
                    Profesor/a
                  </button>
                  <button
                    type="button"
                    className={`auth-rol-option ${rol === 'alumno' ? 'selected' : ''}`}
                    onClick={() => setRol('alumno')}
                    disabled={isLoading}
                  >
                    Alumno/a
                  </button>
                </div>
              </div>

              <div className="auth-field">
                <label htmlFor="nombre" className="auth-label">Nombre</label>
                <input
                  id="nombre"
                  name="nombre"
                  type="text"
                  className="auth-input"
                  placeholder="Tu nombre completo"
                  disabled={isLoading}
                  autoComplete="name"
                />
              </div>
            </>
          )}

          <div className="auth-field">
            <label htmlFor="email" className="auth-label">Email</label>
            <input
              id="email"
              name="email"
              type="email"
              className="auth-input"
              placeholder="tu@email.com"
              disabled={isLoading}
              autoComplete="email"
            />
          </div>

          <div className="auth-field">
            <label htmlFor="password" className="auth-label">Contraseña</label>
            <div className="auth-password-wrapper">
              <input
                id="password"
                name="password"
                type={showPassword ? 'text' : 'password'}
                className="auth-input"
                placeholder="Min. 8 caracteres"
                disabled={isLoading}
                autoComplete={isSignup ? 'new-password' : 'current-password'}
              />
              <button
                type="button"
                className="auth-eye"
                onClick={() => setShowPassword(!showPassword)}
                tabIndex={-1}
              >
                {showPassword ? (
                  <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
                    <path d="M17.94 17.94A10.07 10.07 0 0 1 12 20c-7 0-11-8-11-8a18.45 18.45 0 0 1 5.06-5.94"/>
                    <path d="M9.9 4.24A9.12 9.12 0 0 1 12 4c7 0 11 8 11 8a18.5 18.5 0 0 1-2.16 3.19"/>
                    <line x1="1" y1="1" x2="23" y2="23"/>
                  </svg>
                ) : (
                  <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
                    <path d="M1 12s4-8 11-8 11 8 11 8-4 8-11 8-11-8-11-8z"/>
                    <circle cx="12" cy="12" r="3"/>
                  </svg>
                )}
              </button>
            </div>
          </div>

          <button type="submit" className="auth-submit" disabled={isLoading}>
            {isLoading ? 'Cargando...' : (isSignup ? 'Crear cuenta' : 'Iniciar sesión')}
          </button>
        </form>

        <div className="auth-divider"><span>o</span></div>

        <button
          type="button"
          onClick={() => loginWithGoogle()}
          disabled={isLoading}
          className="auth-google"
        >
          <svg viewBox="0 0 24 24" width="18" height="18">
            <path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" fill="#4285F4"/>
            <path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" fill="#34A853"/>
            <path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" fill="#FBBC05"/>
            <path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" fill="#EA4335"/>
          </svg>
          Google
        </button>

        <button
          type="button"
          className="auth-switch"
          onClick={() => { setIsSignup(!isSignup); setError(null); setRol(null) }}
        >
          {isSignup ? '¿Ya tenés cuenta? Iniciá sesión' : '¿No tenés cuenta? Registrate'}
        </button>
      </div>
    </div>
  )
}
